package jp.co.rich.item.bean;

public class ItemUser {
	private int itemUserId;
	private String itemUserName;
	private String password;

	public int getItemUserId() {
		return itemUserId;
	}

	public void setItemUserId(int itemUserId) {
		this.itemUserId = itemUserId;
	}

	public String getItemUserName() {
		return itemUserName;
	}

	public void setItemUserName(String itemUserName) {
		this.itemUserName = itemUserName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
}
